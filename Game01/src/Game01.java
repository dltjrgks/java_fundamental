import java.util.Scanner;

public class Game01 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		String[] s = { "가위", "바위", "보" };
		int[] score = new int[3];
		String retry;
		System.out.println("***가위 바위 보 게임***");

		do {
			System.out.print("유저 입력 (0-가위, 1-바위, 2-보) : ");
			int user = sc.nextInt();
			int com = (int) (Math.random() * 3);

			System.out.println("유저는 " + s[user] + " 이고, 컴퓨터는 " + s[com] + " 입니다.");

			int result = (user - com + 3) % 3;

			switch (result) {
			/* case -2 : */ case 1:
				System.out.println("User가 승리하였습니다.");
				score[1]++;
				break;
			/* case -1 : */ case 2:
				System.out.println("Computer가 승리하였습니다.");
				score[2]++;
				break;
			case 0:
				System.out.println("비겼습니다.");
				score[0]++;
				break;
			}
			System.out.print("한번 더 하시겠습니까? (Y/N) : ");
			retry = sc.next();
			System.out.println();
			System.out.println();
			if(retry.equals("N")) break;
		} while (retry.equals("Y"));
		
		System.out.println("승 : " + score[1] + " 패  : " + score[2] + " 무승부 : " + score[0]);
		
		sc.close();
	}
}
